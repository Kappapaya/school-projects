public class While04 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int suma = a + b;
        while (suma < 30) {
            System.out.println(suma);
            suma++;
        }
    }
}
